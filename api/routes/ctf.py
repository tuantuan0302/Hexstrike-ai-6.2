logger = logging.getLogger(__name__)

# Create blueprint
ctf_bp = Blueprint('ctf', __name__, url_prefix='/api/ctf')

# Dependencies will be injected via init_app
ctf_manager = None
ctf_tools = None
ctf_automator = None
